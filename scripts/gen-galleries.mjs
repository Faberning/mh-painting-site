// Generates src/galleries.ts from the downloaded files in public/projects/<slug>/.
// Reads real pixel dimensions (sharp) so pages can set aspect-ratio + width/height
// with no crop and no layout shift. Re-run after scripts/fetch-galleries.mjs.
import { readdirSync, statSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import sharp from "sharp";

const ROOT = "public/projects";
const slugs = readdirSync(ROOT).filter((d) => statSync(join(ROOT, d)).isDirectory()).sort();

const map = {};
for (const slug of slugs) {
  const dir = join(ROOT, slug);
  const files = readdirSync(dir)
    .filter((f) => /\.(jpe?g|webp|png)$/i.test(f))
    .sort(); // 01, 02, ... lexical order == display order
  const imgs = [];
  for (const f of files) {
    const meta = await sharp(join(dir, f)).metadata();
    imgs.push({ src: `/projects/${slug}/${f}`, width: meta.width, height: meta.height });
  }
  map[slug] = imgs;
}

const body =
`// AUTO-GENERATED by scripts/gen-galleries.mjs — do not edit by hand.
// Project slug -> ordered gallery images (display order 01, 02, ...).
export interface GalleryImage {
  src: string;
  width: number;
  height: number;
}

export const GALLERIES: Record<string, GalleryImage[]> = ${JSON.stringify(map, null, 2)};

/** First image of a project's gallery, or undefined if none. */
export function leadImage(slug: string): GalleryImage | undefined {
  return GALLERIES[slug]?.[0];
}
`;

writeFileSync("src/galleries.ts", body);
console.log("Wrote src/galleries.ts");
for (const slug of slugs) console.log(`  ${slug.padEnd(30)} ${map[slug].length} images`);
